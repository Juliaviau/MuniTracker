package com.example.MuniTracker;

public class MunicipiInfo {
    public String nom;
    public String comarca;
    public String provincia;
    public String vegueria;

    public MunicipiInfo(String nom, String comarca, String provincia, String vegueria) {
        this.nom = nom;
        this.comarca = comarca;
        this.provincia = provincia;
        this.vegueria = vegueria;
    }
}
